const readmore1 = document.querySelector('.read-more-btn1');
const text1 = document.querySelector('.text1');

readmore1.addEventListener('click', (e)=>{
  text1.classList.toggle('show-more1');
  if(readmore1.innerText ==='Leer menos'){
    readmore1.innerText = ' Leer más';
  }else{
    readmore1.innerText = 'Leer menos';
  }
})

